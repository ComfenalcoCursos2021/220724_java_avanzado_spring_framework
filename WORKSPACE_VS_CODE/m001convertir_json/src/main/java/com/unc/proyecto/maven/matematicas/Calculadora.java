package com.unc.proyecto.maven.matematicas;

public class Calculadora {

    public int suma(int a , int b){
        return a+b;
    }
    public int resta(int a , int b){
        return a-b;
    }
    public int multipliacacion(int a , int b){
        return a*b;
    }
    public int division(int a , int b){
        return a/b;
    }
}
